package com.axone_io.ignition.git.commissioning;

public class RepoConfig {
    private String uri;
    private String branch;

    // Getters and setters
    public String getUri() {
        return uri;
    }

    public void setUri(String uri) {
        this.uri = uri;
    }
    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }
}
